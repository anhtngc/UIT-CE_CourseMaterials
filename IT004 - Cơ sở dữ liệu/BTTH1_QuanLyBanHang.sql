CREATE TRIGGER TRG_HD ON HOADON FOR INSERT, DELETE
AS
BEGIN
	DECLARE @MAKH INT, @TONGTRIGIA INT

	SELECT @TONGTRIGIA = SUM(TRIGIA), @MAKH = MAKH 
	FROM INSERTED 
	GROUP BY MAKH

	UPDATE KHACHHANG
	SET DOANHSO += @TONGTRIGIA
	WHERE MAKH = @MAKH
END

CREATE TRIGGER TR_DEL_HD ON HOADON FOR DELETE
AS
BEGIN
	DECLARE @MAKH INT, @TRIGIA INT

	SELECT @TRIGIA = TRIGIA, @MAKH = MAKH 
	FROM DELETED

	UPDATE KHACHHANG
	SET DOANHSO -= @TRIGIA
	WHERE MAKH = @MAKH
END