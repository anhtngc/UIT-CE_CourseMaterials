#include <iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"Nhap so nguyen a: ";
    cin>>a;
    cout<<"Nhap so nguyen b: ";
    cin>>b;
    cout<<"- Tong cua hai so nguyen: " << a+b << endl;
    cout<<"- Hieu cua hai so nguyen: " << a-b << endl;
    cout<<"- Tich cua hai so nguyen: " << a*b << endl;
    cout<<"- Thuong cua hai so nguyen: " << a/b;
}
